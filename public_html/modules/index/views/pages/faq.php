  <h2>PREPARATION</h2>
  <hr />
  <ul>
    <li>Wear comfortable clothing when you attend your appointment.</li>
    <li>When attending your appointment please remove glasses and/or contacts.</li>
    <li>We advise that not wearing make up allows for a faster appointment.</li>
    <li>Do not apply eye creams or sunblock to the eye area before the treatment.</li>
    <li>Be prepared to remove glasses or contact lenses.</li>
  </ul>
  <hr />
  <h2 class="my-4">AFTER CARE</h2>
  <hr />
  <ul>
    <li>We advise to avoid wetting your lashes for up to 24 hours after application.</li>
    <li>No oil based eye make up or skin care around the lashes.</li>
    <li>Cleanse your lashes with a product safe for lash extensions 2-3 times a week.</li>
    <li>Gently brush through lashes daily with mascara wand provided to avoid any tangles.</li>
    <li>Sleep on your back so as to allow for a longer lash retention.</li>
    <li>Do not attempt to perm, tint or use eye lash curlers whilst wearing eye lash extensions.</li>
  </ul>
  <hr />
  <h2 class="my-2">INFILLS</h2>
  <hr />
  <ul>
    <li>Infills are recommended every two to three weeks to keep your lashes looking full.</li>
    <li>You must have at least 50% lash extensions remaining and have properly cared for your lashes for infills to be done.</li>
    <li>If you are a new client, a full set is required as we only infill lashes applied by a Saint technician.</li>
  </ul>
